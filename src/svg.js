class SVGManager {
  isMounted = false;
  constructor() {}

  mount() {
    if (this.isMounted) {
      return;
    }
    this.isMounted = true;
    // Create a new SVG element to define all the SVG defs
    const svg = createElement({
      tag: "svg",
      xmlns: SVG_NAMESPACE,
      style: { display: "none" },
      id: "xgroups-defs",
    });
    const defs = createElement({ tag: "defs" });
    svg.appendChild(defs);
    const SVGS = SVGDefs;
    for (const [name, conf] of Object.entries(SVGS)) {
      var symbol = createElement({
        tag: "symbol",
        id: name,
        viewBox: conf.viewBox,
        innerHTML: conf.svg,
      });
      defs.appendChild(symbol);
    }
    document.body.prepend(svg);
  }

  unmount() {
    const svg = document.getElementById("xgroups-defs");
    if (svg) {
      svg.remove();
    }
  }

  get(name, props = {}) {
    if (!SVGDefs[name]) {
      console.warn(`SVG icon ${name} not found`);
      return null;
    }
    this.mount();
    return createElement({
      ...props,
      tag: "svg",
      viewBox: SVGDefs[name].viewBox,
      children: [{ tag: "use", "xlink:href": `#${name}` }],
    });
  }
}

export const SVG = new SVGManager();
